# Taking first name from the user
first_name = input("Enter your First Name: ")

# Taking last name from the user
last_name = input("Enter your Last Name: ")

# Concatenating both names with a space
full_name = first_name + " " + last_name

# Displaying the full name
print("Full Name:", full_name)

# Displaying the length of the full name
print("Length:", len(full_name))

# Displaying the first character
print("First Character:", full_name[0])

# Displaying the last character
print("Last Character:", full_name[-1])