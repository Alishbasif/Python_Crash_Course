# Assignment Operators

num = 10
print("Initial Value:", num)

# +=
num += 5
print("After += :", num)

# -=
num -= 3
print("After -= :", num)

# *=
num *= 2
print("After *= :", num)

# /=
num /= 4
print("After /= :", num)

# %=
num %= 3
print("After %= :", num)

# **=
num **= 2
print("After **= :", num)