# Integer
# =========================================
age = 23
print("Age:", age)
print("Data Type:", type(age))


# =========================================
# Float
# =========================================
price = 49.0
print("Price:", price)
print("Data Type:", type(price))


# =========================================
# String
# =========================================
name = "Ali"
print("Name:", name)
print("Data Type:", type(name))


# =========================================
# None Type
# =========================================
student_name = None
print("Student Name:", student_name)
print("Data Type:", type(student_name))

# =========================================
# Creating a Complex Number
# =========================================
complex_num = 2 - 8j

# Display the value
print("Complex Number:", complex_num)

# Display the data type
print("Data Type:", type(complex_num))

# =========================================
# Sum Program
