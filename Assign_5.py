# Logical Operators in Python

a = 10
b = 5

# Boolean Expressions
exp1 = a > b      # True
exp2 = b > 8      # False

# and Operator
print("exp1 and exp2 =", exp1 and exp2)

# or Operator
print("exp1 or exp2 =", exp1 or exp2)

# not Operator
print("not exp1 =", not exp1)
print("not exp2 =", not exp2)