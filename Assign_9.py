# Taking a word from the user
word = input("Enter a word: ")

# Printing the first 3 characters
print("First 3 Characters:", word[:3])

# Printing the last 3 characters
print("Last 3 Characters:", word[-3:])

# Printing the complete string
print("Complete String:", word[:])

# Printing the string without the first character
print("Without First Character:", word[1:])

# Printing the string without the last character
print("Without Last Character:", word[:-1])