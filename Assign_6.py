# Type Conversion

a = 5
b = 2.5

result = a + b

print("Result:", result)
print("Data Type:", type(result))

# Type Casting

a = 10
b = "20"
c = "15.5"

# Convert b into int and add it to a
sum = a + int(b)
print("Sum:", sum)

# Convert c into float
c_float = float(c)
print("Data Type of c:", type(c_float))

# Convert a into string
a_str = str(a)
print("Data Type of a:", type(a_str))